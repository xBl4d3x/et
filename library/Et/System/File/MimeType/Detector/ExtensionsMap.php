	/**
	 * @param array $mime_types_by_extension
	 * @param bool $merge [optional]
	 *
	 * @throws Debug_Assert_Exception when invalid argument is passed
	 */
	public function setMimeTypesByExtension(array $mime_types_by_extension, $merge = true) {
		if(!$merge){
			$this->mime_types_by_extension = array();
		}

		foreach($mime_types_by_extension as $extension => $mime_type){
			$this->setMimeTypeByExtension($extension, $mime_type);
		}
	}

	/**
	 * @return array
	 */
	public function getMimeTypesByExtension() {
		return $this->mime_types_by_extension;
	}

	/**
	 * @param string $extension
	 * @param string $mime_type	 *
	 *
	 */
	function setMimeTypeByExtension($extension, $mime_type){
		$this->mime_types_by_extension[trim(strtolower($extension), " .")] = (string)$mime_type;
	}

	/**
	 * @param string $extension
	 *
	 * @return string|bool
	 * @throws Debug_Assert_Exception
	 */
	function getMimeTypeByExtension($extension){
		if(isset($this->mime_types_by_extension[$extension])){
			return $this->mime_types_by_extension[$extension];
		}

		$ext_lc = trim(strtolower($extension), " .");
		if(isset($this->mime_types_by_extension[$ext_lc])){
			return $this->mime_types_by_extension[$ext_lc];
		}


		return false;
	}

